

class Calculator:
    
    def addition(self, num1, num2):
        total =  num1 + num2
        return total
    
    def sub(self, num1, num2):
        total =  num1 - num2
        return total
    
    def divide(self, num1, num2):
        total =  num1 / num2
        return total
    
    def multiply(self, num1, num2):
        total =  num1 * num2
        return total